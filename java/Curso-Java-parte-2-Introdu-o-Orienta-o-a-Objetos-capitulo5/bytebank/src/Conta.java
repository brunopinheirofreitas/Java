public class Conta {

	private double saldo; //atributos de uma classe//
	private int agencia; //O atributo é encapsulado como PRIVATE pois assim quando do instanciamento de uma classe só é possível alterar seus objetos via método//
	private int numero;
	private Cliente titular;
	private static int total; //criando um atributo que contará a quantidade de vezes que a classe conta foi instanciada. O Static serve para isso, funciona como se fosse uma variável global//
	
	//Os métodos são as interfaces de um objeto//

	public Conta(int agencia, int numero) {
		Conta.total++;
		this.agencia = agencia;
		this.numero = numero;
		System.out.println("Criando o objeto Conta com agencia: "+this.agencia+" e numero de conta: "+this.numero);
		System.out.println("Quantidade de contas na base: "+Conta.total);
	}
	
	public Conta () {
		
	}
	
	
	public void deposita(double valor) { //metodo para depositar//
		if (valor <= 0) {
			System.out.println("Saldo não pode ser um numero negativo!");
			return;
		}
		this.saldo = this.saldo + valor;
	}

	public boolean saca(double valor) { //metodo para sacar, verifica se o valor sacado é < que o saldo//
		if (this.saldo >= valor) {
			this.saldo -= valor;
			return true;
		} else {
			return false;
		}
	}

	public boolean transfere(double valor, Conta destino) { //método que transfere valores de uma conta para outra//
		if (this.saldo >= valor) {
			this.saldo -= valor;
			destino.deposita(valor);
			return true;
		}
		return false;
	}
//Normalmente se cria um GET para "buscar" informações em uma classe e um método SET para incluir dados em um atributo de um objeto//
	public double getSaldo() { //método que informa o saldo de uma conta//
		return this.saldo;
	}

	public int getNumero() { //método que informa o número de uma conta//
		return this.numero;
	}

	public void setNumero(int numero) { //método que recebe o número de uma conta//
		this.numero = numero;
	}

	public int getAgencia() { //método que informa o número da agência de uma conta//
		return this.agencia;
	}

	public void setAgencia(int agencia) { //método que recebe o número da agência de uma conta//
		if (agencia <= 0) {
			System.out.println("Agencia não pode ser um numero negativo!");
			return;
		}
		this.agencia = agencia;
	}

	public void setTitular(Cliente titular) { //método que recebe o nome do cliente titular da conta//
		this.titular = titular;
	}

	public Cliente getTitular() { //método que informa o nome do cliente titular da conta//
		return this.titular;
	}
	
	public static int getTotal() { //A informação STATIC serve para definir que o método é da classe//
		return Conta.total;
	}

}